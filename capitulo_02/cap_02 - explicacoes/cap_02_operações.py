'''
Existem diversas operações com podemos fazer em Python
'''

# Adição (+)
# Subtração (-)
# Multiplicação (*) """Operações Básicas"
# Divisão (/)

n1 = 10
n2 = 5

# Adição
print(n1+n2)

# Subtração
print(n1-n2)

# Multiplicação
print(n1*n2)

# Divisão
print(n1/n2)

# Módulo ou Resto de divisão (%)
print(n1%n2)

# Divisão inteira
print(n1//n2)

# Potencia
print(n1**n2)